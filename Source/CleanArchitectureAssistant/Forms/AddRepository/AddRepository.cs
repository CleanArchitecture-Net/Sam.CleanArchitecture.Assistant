    [Guid("b08d5ade-e3f1-67a2-e6f2-1265d01d6e70")]
    internal class Pane : ToolkitToolWindowPane
    {
        public Pane()
        {
            BitmapImageMoniker = KnownMonikers.ToolWindow;
        }
    }
}
